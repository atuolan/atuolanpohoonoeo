// 主函數：動態組裝 prompt
// ============================================================
export function buildAstroDiceInterpretationPrompt(
  question: string,
  result: AstroDiceResult,
): string {
  const { planet, sign, house } = result;

  // 動態調取對應條目
  const planetDetail =
    PLANET_DETAIL[planet.id] ?? `${planet.nameCn}：${planet.description}`;
  const signDetail =
    SIGN_DETAIL[sign.id] ?? `${sign.nameCn}：${sign.description}`;
  const houseDetail =
    HOUSE_DETAIL[house.id] ?? `${house.nameCn}：${house.description}`;

  const elementMap: Record<string, string> = {
    fire: "火象",
    earth: "土象",
    air: "風象",
    water: "水象",
  };
  const modalityMap: Record<string, string> = {
    cardinal: "開創",
    fixed: "固定",
    mutable: "變動",
  };

  return `你是一位精通占星骰子的占星師。請根據以下擲骰結果，為問卜者提供深入、多角度的解讀。

━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━
【問卜者的問題】
${question}

━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━
【擲骰結果】

行星：${planet.symbol} ${planet.nameCn}（${planet.name}）
${planetDetail}

星座：${sign.symbol} ${sign.nameCn}（${sign.name}）｜${elementMap[sign.element] ?? sign.element}｜${modalityMap[sign.modality] ?? sign.modality}
${signDetail}

宮位：${house.romanNumeral} ${house.nameCn}（${house.name}）
${houseDetail}

━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━
${READING_FRAMEWORK}
━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━

【解讀要求】
- 語氣溫和直接，像朋友聊天一樣自然
- 三顆骰子要一起看，不要分開逐一解釋
- 平衡呈現正面與需要注意的面向
- 結尾給出具體可行的行動建議或心態調整
- 長度 2000 字以內，不要太長也不要太短
- 若問題是是非題，委婉轉向「這件事的狀態/能量是什麼」

請開始你的解讀：`;
}
