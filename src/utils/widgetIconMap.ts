/**
 * 依名稱（label 或圖標 ID）取得圖標組件，找不到時回退到 MessageCircle。
 */
export function resolveWidgetIcon(name?: string | null): Component {
  if (!name) return MessageCircle;
  return WIDGET_ICON_MAP[name] || MessageCircle;
}
